@router.message(Command(commands=["cancel"]))
@router.message(F.text.lower() == "–æ—Ç–º–µ–Ω–∞")
async def cmd_cancel(message: Message, state: FSMContext):

    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É /cancel –∏ —Å–æ–æ–±—â–µ–Ω–∏–µ "–û—Ç–º–µ–Ω–∞"
    –û—á–∏—â–∞–µ—Ç —Ç–µ–∫—É—â–µ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–∏ –æ—Ç–º–µ–Ω—ã

    Args:
        message (Message): –û–±—ä–µ–∫—Ç —Å–æ–æ–±—â–µ–Ω–∏—è.
        state (FSMContext): –û–±—ä–µ–∫—Ç FSMContext.

    Returns:
        None
    """

    await state.clear()
    await message.answer(
        text="–ó–∞–¥–∞—á–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞",
        reply_markup=reply.main
    )
