        "–°–ø–∞—Å–∏–±–æ, —á—Ç–æ —Ä–µ—à–∏–ª –≤–æ—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è –Ω–∞—à–∏–º —Å–µ—Ä–≤–∏—Å–æ–º! –í–æ—Ç –∑–∞–¥–∞—á–∏, –∫–æ—Ç–æ—Ä—ã–µ –º—ã –º–æ–∂–µ–º —Ä–µ—à–∏—Ç—å:\n\n"
        "<b>1) –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ –æ–±—â–µ–π –ö–ü–í:</b>\n"
        "   –í–≤–æ–¥–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π –æ–±—ä–µ–º –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–∞ —Ç–æ–≤–∞—Ä–æ–≤ –ê –∏ –ë –¥–ª—è 1 –∏ 2 –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª—è, "
        "–∞ –º—ã –ø–æ—Å—Ç—Ä–æ–∏–º –≥—Ä–∞—Ñ–∏–∫ –æ–±—â–µ–π –ö–ü–í!\n\n"
        "<b>2) –ù–∞—Ö–æ–∂–¥–µ–Ω–∏–µ —Ç–æ—á–∫–∏ —Ä—ã–Ω–æ—á–Ω–æ–≥–æ —Ä–∞–≤–Ω–æ–≤–µ—Å–∏—è:</b>\n"
